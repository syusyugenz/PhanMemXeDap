    void fillTableHoaDon() {
        DecimalFormatSymbols symbols = DecimalFormatSymbols.getInstance(lc);
        symbols.setCurrencySymbol("VND");
        decimalFormat.setDecimalFormatSymbols(symbols);
        hoaDonModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        hoaDonModel.setColumnIdentifiers(new Object[]{
            "Mã hóa đơn", "Ngày tạo"
        });
        hoaDonModel.setRowCount(0);

        if (dcTimKiem.getDate() == null) {
            key = "";
        } else {
            key = XDate.toString(dcTimKiem.getDate(), "yyyy-MM-dd");
        }

        List<HoaDon> list = hdDAO.selectByDate(key);
        for (HoaDon hd : list) {
            hoaDonModel.addRow(new Object[]{
                hd.getMaHD(), hd.getNgayTao()
            });
        }
        tblHoaDon.setModel(hoaDonModel);
    }

    void fillTableHDCT() {
        int row = tblHoaDon.getSelectedRow();
        DefaultTableModel hdModel = (DefaultTableModel) tblHoaDon.getModel();
        String maHD = (String) hdModel.getValueAt(row, 0);
        List<HoaDonChiTiet> hdct = hdctDAO.selectByHoaDonID(maHD);
        hdctModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        hdctModel.setRowCount(0);
        hdctModel.setColumnIdentifiers(new Object[]{
            "Mã hóa đơn", "Mã sản phẩm", "Tên sản phẩm", "Số lượng", "Giá bán"
        });

        for (HoaDonChiTiet item : hdct) {
            SanPham sp = spDAO.selectById(item.getMaSP());
            hdctModel.addRow(new Object[]{
                item.getMaHD(), sp.getMaSP(), sp.getTenSP(), item.getSoLuong(), decimalFormat.format(item.getGiaBan())
            });
        }
        tblHoaDonChiTiet.setModel(hdctModel);
        HoaDon hd = hdDAO.selectById((Integer) hdModel.getValueAt(row, 0));
        KhachHang kh = khDAO.selectById(hd.getMaKH());
        NhanVien nv = nvDAO.selectById(hd.getMaNV());
        txtTenKH.setText(kh.getTenKH());
        txtTenNV.setText(nv.getHoTen());
        row = -1;
    }

    void fillThongTin() {
        int row = tblHoaDonChiTiet.getSelectedRow();
        DefaultTableModel hdctModel = (DefaultTableModel) tblHoaDonChiTiet.getModel();
        String maHD = (String) hdctModel.getValueAt(row, 0);
        String maSP = (String) hdctModel.getValueAt(row, 1);
        List<HoaDonChiTiet> hdct = hdctDAO.selectByHoaDonIDAndIDSP(maHD, maSP);

    }

    double getTongTien() {
        int row = tblHoaDon.getSelectedRow();
        DefaultTableModel hdModel = (DefaultTableModel) tblHoaDon.getModel();
        String maHD = (String) hdModel.getValueAt(row, 0);
        List<HoaDonChiTiet> hdct = hdctDAO.selectByHoaDonID(maHD);
        double tongTien = 0;
        for (HoaDonChiTiet item : hdct) {
            double thanhTien = item.getSoLuong() * item.getGiaBan();
            tongTien += thanhTien;
        }
        return tongTien;
    }

    void clear() {
        txtTenKH.setText("");
        txtTenNV.setText("");
    }

