    String iconName = "warning.jpg";

    boolean check() {
        boolean flag = true;
        if (txtTenKH.getText().isBlank()) {
            setErrorMessage(lblTenKH, "Vui lòng nhập tên khách hàng!", iconName);
            flag = false;
        } else {
            setErrorMessage(lblTenKH, "", "");
            String pattenName = "^[^!-@]+$";
            if (!txtTenKH.getText().matches(pattenName)) {
                setErrorMessage(lblTenKH, "Sai định dạng tên!", iconName);
                flag = false;
            }
        }
        if (txtSDT.getText().isBlank()) {
            setErrorMessage(lblSDT, "Vui lòng nhập số điện thoại!", iconName);
            flag = false;
        } else {
            setErrorMessage(lblSDT, "", "");
            String pattern = "^(032|033|034|035|036|037|038|039|096|097|098|086|083|084|085|081|082|088|091|094|070|079|077|076|078|090|093|089|056|058|092|059|099)[0-9]{7}$";
            if (!txtSDT.getText().matches(pattern)) {
                setErrorMessage(lblSDT, "SĐT không đúng định dạng!", iconName);
                flag = false;
            }
        }
        return flag;
    }

    public void insert() {

        String tenKH = txtTenKH.getText();
        char[] charArray = tenKH.toCharArray();
        boolean foundSpace = true;
        for (int j = 0; j < charArray.length; j++) {
            if (Character.isLetter(charArray[j])) {
                if (foundSpace) {
                    charArray[j] = Character.toUpperCase(charArray[j]);
                    foundSpace = false;
                }
            } else {
                foundSpace = true;
            }
        }
        tenKH = String.valueOf(charArray);
        KhachHang kh = new KhachHang();
        kh.setTenKH(tenKH);
        kh.setSdt(txtSDT.getText());
        kh.setTichDiem(0);
        khDAO.insert(kh);
        MainThongBao.alert(this, "Thêm khách hàng mới thành công!");

    }

